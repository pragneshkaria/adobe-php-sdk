			  to be downloaded:<br>
			  <ol>
			    <li><a href="http://www.amfphp.org">AMFPHP (1.2.5)</a> - This is installed in the <span class="code">libraries/amfphp/</span> directory. The service browser is not included in this library. If you need that feature, please download AMFPHP and install it into <span class="code">libraries/amfphp/</span>. </li>
	    <li><a href="http://labs.adobe.com/technologies/spry/">Adobe Spry Ajax Framework (1.3)</a> - This is installed in the <span class="code">libraries/spry/</span> directory. <br />
